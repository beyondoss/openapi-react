/**
 * Return type of `useAction`.
 *
 * `status` reflects the most-recent `send` call:
 * - `"idle"` — `send` has never been called (or the path changed).
 * - `"fetching"` — a request is in-flight.
 * - `"success"` — the last `send` completed successfully.
 * - `"error"` — the last `send` failed.
 *
 * `send` returns a Promise that resolves to the response data, or rejects
 * with an `ErrorResponse` on failure.
 */
export type UseActionResult<
  Paths extends {},
  Path extends Extract<keyof Paths, string>,
  Method extends Uppercase<HttpMethod>,
> = {
  status: "idle" | "fetching" | "success" | "error";
  send(
    input: Input<Paths, Path, Lowercase<Method>> extends { input: infer I } ? I
      : void,
    requestInit?: Omit<RequestInit, "method" | "body">,
  ): Promise<Data<Paths, Path, Lowercase<Method>>>;
};

/** A `Response` with a typed `.json()` method. */
export type TypedResponse<T> = Omit<Response, "json"> & {
  json(): Promise<T>;
};

/**
 * The `input` type for a given path + method.
 *
 * Resolves to `{ input: { query?, path?, body? } }` when the operation has
 * parameters or a request body, or `{}` when it has neither. Used to type
 * the `input` field on `load`, `useLoader`, and `fetch`.
 */
// Pick only keys that exist in T, avoiding `unknown` for absent keys
type SafePick<T, K extends string> = Pick<T, Extract<keyof T, K>>;

/**
 * Wraps `Shape` in `{ input: Shape }`, marking `input` as optional iff every
 * field inside `Shape` is itself optional. This lets callers omit `input`
 * entirely for ops where nothing inside it is required (e.g., a `GET` with
 * only optional query params), while still requiring `input` when at least
 * one field inside is required (e.g., a path param).
 */
type WrapInput<Shape> = {} extends Shape ? { input?: Shape } : { input: Shape };

export type Input<
  Paths extends {},
  Path extends Extract<keyof Paths, string>,
  Method extends HttpMethod,
> = Paths[Path] extends Record<Method, { parameters: any }>
  ? Paths[Path][Method]["parameters"] extends {
    query?: never;
    header?: never;
    path?: never;
    cookie?: never;
  } ? Paths[Path][Method] extends { requestBody?: never } ? {}
    : WrapInput<
      Simplify<{ body: RequestBodyOption<Paths[Path][Method]>["body"] }>
    >
  : Paths[Path][Method] extends { requestBody?: never } ? WrapInput<
      ConditionalExcept<
        SafePick<Paths[Path][Method]["parameters"], "query" | "path">,
        undefined
      >
    >
  : WrapInput<
    Simplify<
      ConditionalExcept<
        SafePick<Paths[Path][Method]["parameters"], "query" | "path">,
        undefined
      > & { body: RequestBodyOption<Paths[Path][Method]>["body"] }
    >
  >
  : {};

/** The raw `openapi-fetch` response type for a path + method. */
export type Output<
  Paths extends {},
  Path extends Extract<keyof Paths, string>,
  Method extends HttpMethod,
> = Paths[Path] extends Record<Method, { responses: any }> ? FetchResponse<
    Paths[Path][Method],
    { parseAs: "json" },
    "application/json"
  >
  : never;

/** The success data type for a path + method (excludes `undefined`). */
export type Data<
  Paths extends {},
  Path extends Extract<keyof Paths, string>,
  Method extends HttpMethod,
> = Exclude<Output<Paths, Path, Method>["data"], undefined>;

/** The error body type for a path + method (excludes `undefined`). */
export type ErrorData<
  Paths extends {},
  Path extends Extract<keyof Paths, string>,
  Method extends HttpMethod,
> = Exclude<Output<Paths, Path, Method>["error"], undefined>;

/**
 * Identifies one or more cache entries for `invalidate`, `refetch`, and `purge`.
 *
 * Two forms:
 * - **Exact key** — a path + optional `input` matching a specific cache entry.
 * - **Predicate** — a `match` function called for every entry in the cache.
 *   Receives the parsed cache key and its current subscriber count (`refCount`).
 *
 * @example
 * ```ts
 * // Exact key
 * { path: "GET /users/{id}", input: { path: { id: 42 } } }
 *
 * // All /users entries with active subscribers
 * { match: (key, refCount) => key.path === "GET /users" && refCount > 0 }
 *
 * // Nuke everything (e.g., on logout)
 * { match: () => true }
 * ```
 */
export type PathMatcher<
  Paths extends {},
  Path extends Extract<keyof Paths, string>,
> =
  | ({ path: `GET ${Path}` } & Input<Paths, Path, "get">)
  | {
    match(
      key: { path: `GET ${Path}` } & Input<Paths, Path, "get">,
      refCount: number,
    ): boolean;
  };

export type LoadResult<T extends CachedResponse<any, any>> =
  | {
    status: "error";
    data: undefined | T["data"];
    error: T["error"] extends { code: string; message: string } | undefined
      ? T["error"]
      : never;
    promise: Promise<LoadResult<T>>;
  }
  | {
    status: "success" | "refetching";
    data: T["data"];
    error: undefined;
    promise: Promise<LoadResult<T>>;
  }
  | {
    status: "fetching";
    data: undefined;
    error: undefined;
    promise: Promise<LoadResult<T>>;
  };

/** The subset of `Paths` keys that have a `get` method — the only paths valid for `useLoader` and `load`. */
export type LoadablePaths<Paths extends {}> = Extract<
  PathsWithMethod<Paths, "get">,
  string
>;

type Observer<T> = {
  (state: T): void;
};

type Subject<T> = {
  setState(state: T): void;
  getState(): T;
  observe(observer: Observer<T>): () => void;
  unobserve(observer: Observer<T>): void;
};
