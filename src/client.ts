/**
 * A single entry in the shared cache.
 *
 * Returned by `load`. The `promise` field is useful when you need to await
 * an in-flight request that another caller already started.
 */
export type CachedResponse<
  Paths extends {},
  Path extends Extract<keyof Paths, string>,
> = {
  data: Data<Paths, Path, "get"> | undefined;
  error: ErrorData<Paths, Path, "get"> | undefined;
  response: Response | undefined;
  status: FetchStatus;
  promise: Promise<unknown>;
  createdAt: number;
};

type FetchStatus = "fetching" | "refetching" | "success" | "error";

type ErrorEnvelope = { error?: { message?: string } };

/**
 * Wraps an API error response with a typed `.data` payload.
 *
 * Thrown by `useLoader` (when `suspense: true`), `useAction.send`, and `load`
 * (for non-HTTP errors). Caught by `<ErrorBoundary>` when thrown from a hook.
 *
 * `.message` is extracted from `data.error.message` if present; otherwise
 * falls back to `"API error"`.
 *
 * @example
 * ```ts
 * try {
 *   await api.send({ body: newUser });
 * } catch (err) {
 *   if (err instanceof ErrorResponse) {
 *     console.error(err.data);       // typed error body
 *     console.error(err.response);   // raw Response
 *   }
 * }
 * ```
 *
 * @example
 * ```tsx
 * // ErrorBoundary catches ErrorResponse thrown by useLoader in suspense mode
 * function ErrorFallback({ error }: { error: unknown }) {
 *   if (error instanceof ErrorResponse) {
 *     return <p>API error {error.response?.status}: {error.message}</p>;
 *   }
 *   return <p>Unexpected error</p>;
 * }
 * ```
 */
export class ErrorResponse<T extends ErrorData<any, any, any>> extends Error {
  data: T;
  response: Response | undefined;
  constructor(data: T, response?: Response) {
    super((data as ErrorEnvelope)?.error?.message ?? "API error");
    this.name = "ErrorResponse";
    this.data = data;
    this.response = response;
  }
}

function encodeKey({
  path,
  input,
  ...other
}: {
  path: string;
  input?: Record<string, unknown>;
}) {
  const processed = input ? removeEmptyKeys(input) : undefined;
  return JSON.stringify({
    path,
    input: processed && Object.keys(processed).length
      ? sortObject(processed)
      : undefined,
    ...other,
  });
}

function removeEmptyKeys(obj: Record<string, unknown>) {
  const next: Record<string, unknown> = {};

  for (const key in obj) {
    if (
      obj[key] === undefined
      || (typeof obj[key] === "object"
        && obj[key] !== null
        && !Object.keys(obj[key] as object).length)
    ) {
      continue;
    }

    next[key] = obj[key];
  }

  return next;
}

function sortObject(obj: Record<string, unknown>): unknown {
  if (typeof obj !== "object" || obj === null) {
    return obj;
  }

  if (Array.isArray(obj)) {
    const s = Array.from<unknown>({ length: obj.length });
    for (let i = 0; i < obj.length; i++) {
      s[i] = sortObject(obj[i] as Record<string, unknown>);
    }
    return s;
  }

  const sorted: Record<string, unknown> = {};
  const keys = Object.keys(obj).sort();
  for (let i = 0; i < keys.length; i++) {
    const key = keys[i]!;
    sorted[key] = sortObject(obj[key] as Record<string, unknown>);
  }

  return sorted;
}

const REDACTED_HEADERS = new Set(["authorization", "cookie", "set-cookie"]);

function logResponse(res: { data?: unknown; response: Response }) {
  const { response } = res;
  const headers = Object.fromEntries(
    [...response.headers.entries()].filter(
      ([k]) => !REDACTED_HEADERS.has(k.toLowerCase()),
    ),
  );
  console.log(`📮 %c${response.status} ${response.url}`, "color: #999");
  console.log("Headers\n", headers);
  console.log("Body\n", res.data);
}

